import streamlit as st
import joblib
import pandas as pd
import plotly.express as px

df = pd.read_csv("jobs_in_data.csv")

st.header("Dataset Overview")
col1, col2, col3, col4 = st.columns(4)
with col1:
    st.metric("Records", len(df))
with col2:
    st.metric("Countries", df["employee_residence"].nunique())
with col3:
    st.metric(
        "Average Salary(USD)",
        f"${df["salary_in_usd"].mean():,.0f}"
    )
with col4:
    st.metric(
        "Maximum Salary",
        f"${df["salary_in_usd"].max():,.0f}"
    )
st.divider()

with st.expander("View Dataset"):
    st.dataframe(df.head(10))

#salary Insights
#plotly bar chart for average salary by job category
st.subheader("Average Salary by Job Category")

salary_job = (
    df.groupby("job_category")["salary_in_usd"]
    .mean()
    .sort_values(ascending=False)
    .reset_index()
)

fig = px.bar(
    salary_job,
    x="salary_in_usd",
    y="job_category",
    orientation="h",
    color="salary_in_usd",
    title="Average Salary by Job Category",
    labels={
        "salary_in_usd": "Average Salary (USD)",
        "job_category": "Job Category"
    }
)

st.plotly_chart(fig, use_container_width=True)
#end here
#st.header("Salary Insights")
#job_count = df["job_category"].value_counts()
#st.bar_chart(job_count)

work_setting = df["work_setting"].value_counts()
st.subheader("Work Setting Distribution")
st.bar_chart(work_setting)

exp_level = df["experience_level"].value_counts()
st.subheader("Experience Level Distribution")
st.bar_chart(exp_level)

st.divider()

#load the model 
st.header("Salary Prediction")
model =  joblib.load('model.pkl')

st.set_page_config(
    page_title = "Salary Prediction App",
    page_icon = "💼",
    layout = "wide"
)

#sidebar creation
st.sidebar.title("📌Project Information")
st.sidebar.markdown("""### Model
Random Forest Regressor

### Dataset
Data Science Salaries

### Features
- Work Year
- Job Category
- Employee Residence
- Experience Level
- Employment Type
- Work Setting
- Company Location
- Company Size

### Target
Salary in USD
""")

st.sidebar.markdown("---")
st.sidebar.subheader("📊Model Performance")

st.sidebar.metric("R2 Score :", "0.365")
st.sidebar.metric("RMSE :", "51,779")

st.title("Salary Prediction App")
st.markdown("""Predict the estimated salary of a data professional using a Machine 
            Learning model trained on real-world salary data.""")
st.write("This app predicts the salary of employees based on their job profile and other features.")

#input creation

col1, col2 = st.columns(2)
with col1:
    work_year = st.number_input(
        "Work Year",
        min_value=int(df["work_year"].min()),
        max_value=int(df["work_year"].max()),
        value=int(df["work_year"].max())
    )

    job_category = st.selectbox(
        "Job Category",
        [
            
        'BI and Visualization',
        'Cloud and Database',
        'Data Analysis',
        'Data Architecture and Modeling',
        'Data Engineering',
        'Data Management and Strategy',
        'Data Quality and Operations',
        'Data Science and Research',
        'Leadership and Management',
        'Machine Learning and AI'
        ]
    )

    employee_residence = st.selectbox(
        "Employee Residence",
        sorted(df["employee_residence"].unique())
    )

    work_setting = st.selectbox(
        "Work Setting",
        ["Hybrid", "In-person", "Remote"]
    )
with col2:
    company_location = st.selectbox(
        "Company Location",
        sorted(df["company_location"].unique())
    )

    company_size = st.selectbox(
        "Company Size",
        sorted(df["company_size"].unique())
    )


    experience_level = st.selectbox(
        "Experience Level",
        sorted(df["experience_level"].unique())
    )

    employment_type = st.selectbox(
        "Employment Type",
        sorted(df["employment_type"].unique())
    )

if st.button("Predict Salary"):
    input_data = pd.DataFrame({
        "work_year": [work_year],
        "job_category": [job_category],
        "employee_residence": [employee_residence],
        "experience_level": [experience_level],
        "employment_type": [employment_type],
        "work_setting": [work_setting],
        "company_location": [company_location],
        "company_size": [company_size]
    })

    prediction = model.predict(input_data)
    salary = prediction[0]
    st.subheader("📋 Selected Information")
    st.write(input_data)
    st.success("Prediction Completed Successfully! 🎉")

    st.metric(
        label="Estimated Salary (USD)",
        value=f"${salary:,.2f}"
    )
    st.balloons()
    st.info("This prediction is generated by a Machine Learnig alogorithm and should be considered an estimation rether than guarenteed salary.")